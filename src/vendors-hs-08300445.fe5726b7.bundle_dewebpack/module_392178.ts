/**
 * Converts a string to camelCase format
 * @param input - The string to convert
 * @returns The camelCase version of the input string
 */
function toCamelCase(input: string): string {
    const matches = input.match(WORD_BOUNDARY_REGEX);
    
    if (!matches) {
        return input;
    }
    
    return matches.reduce((result: string, word: string, index: number) => {
        if (index === 0) {
            return result + word.toLowerCase();
        }
        return result + word.charAt(0).toUpperCase() + word.substring(1).toLowerCase();
    }, '');
}

/**
 * Converts a path string to camelCase format while preserving path separators
 * Splits on '/' and applies camelCase to each segment
 * @param path - The path string to convert
 * @returns The camelCase version of the path
 */
export default function convertPathToCamelCase(path: string): string {
    return path.split('/').map(toCamelCase).join('/');
}