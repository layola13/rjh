function resolveLocaleModuleId(localePath: string): number {
  if (!(localePath in localeModuleMap)) {
    const error = new Error(`Cannot find module '${localePath}'`) as NodeJS.ErrnoException;
    error.code = "MODULE_NOT_FOUND";
    throw error;
  }
  return localeModuleMap[localePath];
}

